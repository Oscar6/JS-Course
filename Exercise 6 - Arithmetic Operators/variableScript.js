// Arithmetic Operators

	var x = 100, y = 20;

// Addition (+)
	var a = x + y;

	document.write("Addition: ");
	document.write(a);

	document.write("<br>");

// Subtraction (-)
	var s = x - y;

	document.write("Subtraction: ");
	document.write(s);

	document.write("<br>");

// Multiplication (*)
	var m = x * y;

	document.write("Multiplication: ");
	document.write(m);

	document.write("<br>");


// Division (/)
	var d = x / y;

	document.write("Division: ");
	document.write(d);

	document.write("<br>");

// Modulus %
	var mod = x % y;

	document.write("Modulus: ");
	document.write(mod);

	document.write("<br>");

// Increment ++
	var i = 5;
	++i;

	document.write("Increment: ");
	document.write(i);

	document.write("<br>");

// Decrement --
	var j = 5;
	--j;

	document.write("Decrement: ");
	document.write(j);

	document.write("<br>");

	document.write("<br>");

// Prefix Increment Operator (++a)
	var a = 10;

	document.write("Prefix Increment: ");
	document.write(++a);

	document.write("<br>");

// Postfix Increment Operator (a++)
	var a = 10;

	document.write("Postfix Increment: ");
	document.write(a++);

	document.write("<br>");

	document.write(a);

	document.write("<br>");
// Prefix Decrement Operator (--a)
	var a = 10;

	document.write("Prefix Decrement: ");
	document.write(--a);

	document.write("<br>");
// Postfix Decrement Operator (a--)
	var a = 10;

	document.write("Postfix Decrement: ");
	document.write(a--);

	document.write("<br>");

	document.write(a);



















