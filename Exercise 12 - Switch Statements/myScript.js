// Switch Statements

var studentGrade = "N";

switch (studentGrade) {
	case "A":
		document.write("Student with grade A <br>");
		break;
	case "B":
		document.write("Student with grade B <br>");
		break;
	case "C":
		document.write("Student with grade C <br>");
		break;
	default:
		document.write("No data found");
}