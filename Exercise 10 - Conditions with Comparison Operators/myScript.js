// Conditions with Comparison Operators

// Equal to (==)
var dogAge = "2";

if (dogAge == 2) {
	document.write("Dog age value is equal to 2 <br>");
}

// Equal value & type
// (==) vs (===)
if (dogAge === 2) {
	document.write("Dog age value and type are same and is equal to 2 <br>");
}

// Not equal to (!=)
var catAge = 6;

if (catAge != 6) {
	document.write("Cat age value is not equal to 6 <br>");
}

// Not equal Value & Not Type (!==)
var kittyAge = "9";

if (catAge !== 9) {
	document.write("Kitty age value is not same. <br>");
}

// Greater than (>)
var puppyAge = 10;

if (puppyAge > 5) {
	document.write("Puppy age is greater than 5. <br>");
}

// Less than (<)
var puppyAge = 16;

if (puppyAge < 20) {
	document.write("Puppy age is less than 20. <br>");
}

// Greater than Equal to (>=)
var horseAge = 16;

if (horseAge >= 20) {
	document.write("Horse age is greater than or equal to 20. <br>");
}

// Less than Equal to (>=)
var horseAge = 16;

if (horseAge <= 20) {
	document.write("Horse age is less than or equal to 20. <br>");
}